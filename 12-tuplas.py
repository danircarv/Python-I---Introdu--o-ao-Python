games_tuple = ("Fifa", "Red Dead", "Star Wars"
               , "Mario", "Zelda")
print(games_tuple)
print(type(games_tuple))

# 1 - Buscar os dois primeiros itens da tupla
print(games_tuple[:2])

# 2 - Buscar o último item da lista
print(games_tuple[-1])

# 3 Buscar jogos até uma determinada posição
print(games_tuple[:3])

# 4 - Buscar jogos de uma posição em diante
print(games_tuple[2:])

# 5 - Recuperar um item da tupla pelo indice
print(games_tuple.index("Mario"))



